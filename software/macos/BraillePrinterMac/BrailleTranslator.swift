import Foundation

struct BrailleTranslator {
    func messageBytes(for text: String) -> [UInt8] {
        var bytes: [UInt8] = [0xF0]
        let normalized = text
            .replacingOccurrences(of: "\r\n", with: "\n")
            .replacingOccurrences(of: "\r", with: "\n")

        for character in normalized {
            if let value = Self.dictionary[character] {
                bytes.append(contentsOf: value)
            }
        }

        bytes.append(0xFF)
        return bytes
    }

